package Semana06_Practica;

class Personal {
    String Nombre;
    int Edad;
    int DNI;
    String Pais;
    int Telefono;

    public Personal(String Nombre, int Edad, int DNI, String Pais, int Telefono){
        this.Nombre=Nombre;
        this.Edad=Edad;
        this.DNI=DNI;
        this.Pais=Pais;
        this.Telefono=Telefono;
    }


}
